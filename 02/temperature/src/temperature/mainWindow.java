    private void degreesCelsiusKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_degreesCelsiusKeyReleased
        String typed = degreesCelsius.getText();
        jSlider1.setValue(0);
        if(!typed.matches("\\d+") && !typed.matches("-\\d+") || typed.length() > 3){
            return;
        }
        jSlider1.setValue(Integer.parseInt(typed));
    }//GEN-LAST:event_degreesCelsiusKeyReleased

