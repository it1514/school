    private void jSlider1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider1StateChanged
        degreesCelsius.setText(String.valueOf(jSlider1.getValue()));
        int celsius = jSlider1.getValue();
        String fahren = String.valueOf(celsius*9/5+32);
        degreesFahrnheit.setText(fahren);
        String kelvin = String.valueOf(celsius+273);
        degreesKelvin.setText(kelvin);
        int color = celsius+50;
        if(color>50){
            color = color-50;
            int r = 255 - (int)(color*(float) 255/50);
            colorPanel.setBackground(new Color(255, r, r));
            
        }
        else {
            int b = (int)(color * (float) 255/50);
            colorPanel.setBackground(new Color(b,b,255));
        }
        
    }//GEN-LAST:event_jSlider1StateChanged

